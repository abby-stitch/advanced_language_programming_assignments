void convert(int n)
{
	int isMinus = 0;
	if (n < 0)
	{
		isMinus = 1;
		if (n == -2147483647 - 1)
			isMinus = 0;
		else
		{
			n = -n;
		}
	}
	if (n == 0);
	else
	{
		if (n % 10 < 0)
			printf("%c ", (char)(-(n % 10) + 48));
		else
			printf("%c ", (char)(n % 10 + 48));
		convert(n / 10);
	}
	if (isMinus)
		printf("-");

}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：main函数不准动
***************************************************************************/
int main()
{
	int n;
	printf("请输入一个整数\n");
	scanf("%d", &n);

	convert(n);
	printf("\n");

	return 0;
}
